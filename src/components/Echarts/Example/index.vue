<template>
    <div class="example" ref="example"></div>
</template>

<script>
import * as echarts from "echarts";
import { onMounted, ref } from "vue";
export default {
  name: "Example",
  setup() {
    let example = ref(null);

    onMounted(() => {
      // 基于准备好的dom，初始化echarts实例
      console.log(example);
      var myChart = echarts.init(example.value);
      // 绘制图表
      myChart.setOption({
        title: {
          text: "ECharts 入门示例",
        },
        tooltip: {},
        xAxis: {
          data: ["衬衫", "羊毛衫", "雪纺衫", "裤子", "高跟鞋", "袜子"],
        },
        yAxis: {},
        series: [
          {
            name: "销量",
            type: "bar",
            data: [5, 20, 36, 10, 10, 20],
          },
        ],
      });
    });

    return {
      example,
    };
  },
};
</script>

<style>
.example{
  width: 100%;
  height: 100%;
}
